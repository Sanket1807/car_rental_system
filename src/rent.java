    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            String date = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
            int days = (int) jSpinner1.getValue();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mainproject", "root", "");
            Statement stmt = con.createStatement();
            String p = "insert into rent(contact,carmodel,rentdate,days,totalrent,rentstatus) values(?,?,?,?,?,?)";
            PreparedStatement ps = con.prepareStatement(p);
            if (date.equals("") || jTextField1.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please fill the fields");
            } else {
                ps.setString(1, jTextField1.getText());
                ps.setString(2, jLabel3.getText());
                ps.setString(3, date);
                ps.setInt(4, days);
                ps.setString(5, jLabel6.getText());
                ps.setString(6, "Yes");
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Booking done successfully...");
                new Login().setVisible(true);
                setVisible(false);
            }
        } catch (SQLException | ClassNotFoundException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

