    public rent(String model, String type) {
        this();
        jLabel3.setText(model);
        types = type;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mainproject", "root", "");
            Statement stmt = con.createStatement();
            String q = "select * from rent where carmodel='" + jLabel3.getText() + "'";
            ResultSet rs = stmt.executeQuery(q);
            while (rs.next()) {
                if (rs.getString(6).equals("Yes")) {
                    JOptionPane.showMessageDialog(this, "Car Unavailable");
                    new home().setVisible(true);
                    setVisible(false);
                } else {
                    new rent(model, type).setVisible(true);
                    setVisible(false);
                }
            }
        } catch (SQLException | ClassNotFoundException e) {
            e.printStackTrace();
        }
    }

